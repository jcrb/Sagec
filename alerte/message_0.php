
<H1>INFORMATIONS</H1>
<p>
La DHOS et l'INVS ont modifié la liste des informations épidémiologiques que doivent faire remonter quotidiennement les ARH. La principale modification concerne le nombre de PLACES inoccupées. C'est pourquoi, sur le bordereau de saisie, vous trouverez en plus des lits disponibles, une zone de saisie pour les places disponibles.
</P>
<P>
Par ailleurs, il est désormais impératifs que les informations soient saisies au plus tard pour 14h afin que les données puissent être transmises pour 15h.
</P>

